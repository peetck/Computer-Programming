#include <stdio.h>
#include <math.h>
int main(){
    double first, sec;
    scanf("%lf,%lf", &first, &sec);
    printf("The sum of the given numbers : %lf\n", first + sec);
    printf("The difference of the given numbers : %lf\n", first - sec);
    printf("The product of the given numbers : %lf\n", first * sec);
    printf("The quotient of the given numbers : %lf\n", first / sec);
}